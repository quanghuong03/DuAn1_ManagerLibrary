package DomainModels;

/**
 *
 * @author Apc
 */
public class NhaXuatBan {
    private String id;
    private String ten;
    private String diaChi;

    public NhaXuatBan() {
    }

    public NhaXuatBan(String id, String ten, String diaChi) {
        this.id = id;
        this.ten = ten;
        this.diaChi = diaChi;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getDiaChi() {
        return diaChi;
    }

    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }
    
    
}
