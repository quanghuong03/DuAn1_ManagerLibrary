    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonActionPerformed
        // TODO add your handling code here:
        FrmPhieuNhap._lstTacGia = this.getLstTacGia();
        FrmSach._lstTacGia = this.getLstTacGia();
        String tacGiaChon = "";
        for (TacGia tacGia : FrmPhieuNhap._lstTacGia) {
            tacGiaChon += tacGia.getHoTen()+"-";
        }
        tacGiaChon = tacGiaChon.substring(0, tacGiaChon.length()-1);
        if(!tacGiaChon.equals("") && FrmPhieuNhap.Frm==1){
            FrmPhieuNhap.lblTacGia.setText(tacGiaChon);
            FrmPhieuNhap.Frm=0;
        }else{
            FrmSach.lblTacGia.setText(tacGiaChon);
        }
        this.dispose();
    }//GEN-LAST:event_btnChonActionPerformed

