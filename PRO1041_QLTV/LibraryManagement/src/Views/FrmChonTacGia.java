    private void setListTacGia(){
        FrmPhieuNhap._lstTacGia = new ArrayList<>();
        FrmSach._lstTacGia = new ArrayList<>();
        List<TacGia> _lst = SERVICE.getAll();
        _lst.sort((o1, o2) -> {
            return o1.getHoTen().compareTo(o2.getHoTen());
        });
        for (TacGia tacGia : _lst) {
            FrmTacGiaKhung frmKhung = new FrmTacGiaKhung();
            frmKhung.setDisPlayForm(tacGia);
            pnListTacGia.add(frmKhung);
        }
        int size = (pnListTacGia.getComponentCount()/3)*120 +5;
        pnListTacGia.setPreferredSize(new Dimension(WIDTH, size));
        pnListTacGia.validate();
        pnListTacGia.repaint();
    }
    
    private List<TacGia> getLstTacGia(){
        int all = pnListTacGia.getComponentCount();
        List<FrmTacGiaKhung> _lstKhung = new ArrayList<>();
        List<TacGia> _lst = new ArrayList<>();
        for (int i = 0; i < all; i++) {
            FrmTacGiaKhung frm = (FrmTacGiaKhung) pnListTacGia.getComponent(i);
            if(frm.isSelected()){
                _lstKhung.add(frm);
            }
        }
        for (FrmTacGiaKhung frmTacGiaKhung : _lstKhung) {
            _lst.add(SERVICE.getByMa(frmTacGiaKhung.getMaTacGia()));
        }
        return _lst;
    }

