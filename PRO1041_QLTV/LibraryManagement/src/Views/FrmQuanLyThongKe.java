    // Hàm set hiển thị và nội dung table
    private void setTable(){
        JTableHeader header = tblThongKe.getTableHeader();
        header.setBackground(new Color(125, 200, 150));
        this.setTableSachDuocMuon();
    }
    
    //hàm lấy mẫu table
    private DefaultTableModel setContentTable(String ...args){
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        for (String arg : args) {
            model.addColumn(arg);
        }
        return model;
    }
    
    //Hàm lấy mẫu table theo sách được mượn
    private void setTableSachDuocMuon(){
        DefaultTableModel model = setContentTable("TÊN ĐỘC GIẢ", "MÃ SÁCH", "TÊN SÁCH", "Số lượng đang mượn");
        tblThongKe.setModel(model);
    }
    
    //Hàm lấy mẫu table theo sách được mượn
    private void setTableSachYeuThich(){
        List<SachYeuThich> _lst = service.getSachYeuThich();
        DefaultTableModel model = setContentTable("MÃ SÁCH", "TÊN SÁCH", "LƯỢT MƯỢN");
        for (SachYeuThich sachYeuThich : _lst) {
            String rowdata[] = {
              sachYeuThich.getMasach(), sachYeuThich.getTenSach(), sachYeuThich.getLuotMuon()+""
            };
            model.addRow(rowdata);
        }
        tblThongKe.setModel(model);
    }
    
    //Hàm fill ngày tháng năm
    private void fillcbxMDY(JComboBox ngay, JComboBox thang, JComboBox nam){
        int check = Integer.parseInt((String) thang.getSelectedItem());
        switch (check) {
            case 1, 3, 5, 7, 8, 10 -> {
                ngay.removeAllItems();
                for (int i = 1; i <= 31; i++) {
                    ngay.addItem(i);
                }
            }
            case 4, 6, 9, 11 ->{
                ngay.removeAllItems();
                for (int i = 1; i <= 30; i++) {
                    ngay.addItem(i);
                }
            }case 2 ->{
                int checkNam = Integer.parseInt((String) nam.getSelectedItem());
                if((checkNam%4==0 && checkNam%100!=0) || (checkNam%400==0)){
                    ngay.removeAllItems();
                    for (int i = 1; i <= 29; i++) {
                        ngay.addItem(i);
                    }
                }else{
                    ngay.removeAllItems();
                    for (int i = 1; i <= 28; i++) {
                        ngay.addItem(i);
                    }
                }
            }
        }
    }

